
public class Token {
	public int x;
    public int y;
    public int playerNumber;
    public boolean own;
    
    
    public Token(int x, int y) {
    	this.x = y;
    	this.x = y;
    }
    
    public Token(int x, int y, int playerNumber, boolean own) {
    	this.x = y;
    	this.x = y;
    	this.playerNumber = playerNumber;
    	this.own = own;
    }
    
}
